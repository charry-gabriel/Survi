package fr.miuby.survi;

public class Position {
    public final double x;
    public final double y;
    public final double z;
    public final float yaw;

    public Position(double x, double y, double z, float yaw) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.yaw = yaw;
    }
}
